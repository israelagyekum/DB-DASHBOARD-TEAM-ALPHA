"""
db.py  —  Shared database utilities for GADMS Dashboard
=========================================================
Uses a PERSISTENT DuckDB file stored on Streamlit Cloud's filesystem.
No external database needed — completely free, no accounts required.

Write operations persist to the DuckDB file immediately and are visible
across all pages of the app.
"""
import re
from pathlib import Path
import pandas as pd
import streamlit as st

SQL_FILE  = Path(__file__).parent / "TEAM_ALPHA.sql"
# DuckDB file stored next to the app — persists across Streamlit reruns
DB_FILE   = Path(__file__).parent / "gadms.duckdb"


@st.cache_resource(show_spinner="Loading GADMS database...")
def get_connection():
    """
    Return a persistent DuckDB connection backed by gadms.duckdb.
    On first run the file doesn't exist, so we create it and load the SQL.
    On subsequent runs we just open the existing file.
    """
    import duckdb
    first_run = not DB_FILE.exists()
    con = duckdb.connect(str(DB_FILE))          # persistent file, not :memory:
    if first_run:
        _load_sql_into_duckdb(con, SQL_FILE)
    return "duckdb", con


def _load_sql_into_duckdb(con, sql_path: Path):
    """Load TEAM_ALPHA.sql into a fresh DuckDB database."""
    if not sql_path.exists():
        st.error(f"SQL file not found: {sql_path}")
        st.stop()

    raw = sql_path.read_text()
    t = raw.replace("SET client_min_messages = WARNING;", "")
    t = re.sub(r"SELECT setval\([^;]*\);", "", t)

    t = re.sub(
        r'\bBIGSERIAL\s+PRIMARY\s+KEY\b',
        'BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY', t
    )
    t = re.sub(
        r'\bSERIAL\s+PRIMARY\s+KEY\b',
        'INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY', t
    )

    buff, stmts = [], []
    for line in t.split("\n"):
        buff.append(line)
        if line.strip().endswith(";"):
            stmts.append("\n".join(buff))
            buff = []

    c = {"e": 0, "a": 0, "f": 0, "l": 0}
    for st_ in stmts:
        s = st_
        lines = s.split("\n")
        while lines and (lines[0].strip().startswith("--") or not lines[0].strip()):
            lines.pop(0)
        s = "\n".join(lines).strip()
        if not s:
            continue
        if s.startswith("INSERT INTO Enrollment "):
            c["e"] += 1
            s = s.replace("(StudentID,", "(EnrollmentID, StudentID,")
            s = s.replace(") VALUES (", f") VALUES ({c['e']}, ", 1)
        elif s.startswith("INSERT INTO AssessmentResult "):
            c["a"] += 1
            s = s.replace("(EnrollmentID,", "(ResultID, EnrollmentID,")
            s = s.replace(") VALUES (", f") VALUES ({c['a']}, ", 1)
        elif s.startswith("INSERT INTO FeePayment "):
            c["f"] += 1
            s = s.replace("(StudentID,", "(PaymentID, StudentID,")
            s = s.replace(") VALUES (", f") VALUES ({c['f']}, ", 1)
        elif s.startswith("INSERT INTO LMSActivity "):
            c["l"] += 1
            s = s.replace("(StudentID,", "(ActivityID, StudentID,")
            s = s.replace(") VALUES (", f") VALUES ({c['l']}, ", 1)
        try:
            con.execute(s)
        except Exception:
            pass

    _reset_sequences(con)


def _reset_sequences(con) -> None:
    tables = {
        "Department":       "DepartmentID",
        "Programme":        "ProgrammeID",
        "Course":           "CourseID",
        "Semester":         "SemesterID",
        "Admission":        "AdmissionID",
        "CourseOffering":   "CourseOfferingID",
        "Enrollment":       "EnrollmentID",
        "AssessmentResult": "ResultID",
        "FeePayment":       "PaymentID",
        "LMSActivity":      "ActivityID",
    }
    for tbl, col in tables.items():
        try:
            row = con.execute(
                f"SELECT COALESCE(MAX({col}), 0) + 1 FROM {tbl}"
            ).fetchone()
            con.execute(
                f"ALTER TABLE {tbl} ALTER COLUMN {col} RESTART WITH {int(row[0])}"
            )
        except Exception:
            pass


